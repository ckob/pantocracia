					<h2 id="faq1">¿Qué es Pantocracia?</h2>
					<br>
					<p>Pantocracia es, actualmente, una utópica idea de gobierno democratico. El concepto nace a partir de la necesidad de crear un trabajo para DAM en l'Escola del Treball en la asignatura de EIE y añadiendo también la posibilidad de hacer paralelamente la página web final para HTML.</p>
					<br><br>
					<h2 id="faq2">El nombre</h2>
					<br>
					<p>El término Pantocracia proviene de las palabras griegas Pan o <b>Panto</b>, que significa <b>todos</b>, y <b>cracia</b>, que significa <b>gobierno</b>. En otras palabras, significa gobierno de todos, donde todos los ciudadanos nos podemos implicar.</p>
					<br><br>
					<h2 id="faq3">La idea</h2>
					<br>
					<p>En la actual democracia, el sistema nos permite elegir cada cierto tiempo a unos representantes que tomarán decisiones y votarán en representación nuestra hasta que el propio sistema nos permite volver a elegir. Esta manera, teóricamente correcta y legal, es muy complicado que nos complazca a todos, incluso a los votantes del partido que nos representa, en todas sus decisiones.</p>
					<br>
					<p>Con la actual tecnología somos capaces de crear un sistema que nos permita a todos los ciudadanos formar parte de las decisiones más importantes de Europa, nuestro país, nuestra comunidad autónoma e incluso nuestra ciudad.</p>
					<br><br>
					<h2 id="faq4">El sistema</h2>
					<br>
					<p>Las votaciones más importantes que se lleven a cabo en el congreso de los diputados se trasladarán a toda la población con capacidad legal para votar mediante Pantocracia. Estas personas podrán ejercer ese derecho a través de Internet, móvil, bibliotecas, cajeros automáticos, etc.</p>
					<br>
					<p>La segunda utilidad a destacar del sistema sería la posibilidad de trasladar propuestas al congreso de los diputados. El pueblo podría añadir sus propuestas a través de los distintos canales. Estas propuestas serían votadas por los propios ciudadanos y aproximadamente cada semana se llevarían las, por ejemplo, 10 mejores propuestas (las mas votadas) al congreso de los diputados.</p>
					<br>
					<p>Todo esto se realizaría a través de un sistema de credencial seguro mediante DNI y clave de acceso o huella digital en los dispositivos que lo permitieran. En cuanto tecnológicamente la acreditación por huella digital fuera accesible a todo el mundo (u otro sistema mejor), se eliminaría la autenticación por clave de acceso para evitar la posible cesión de claves a terceras personas.</p>
					<br>
					<p>La seguridad informática sería un factor muy a tener en cuanta para que hubiera ningún tipo de fraude posible. Cualquier fraude o intento de ello sería castigado duramente por la Ley.</p>
